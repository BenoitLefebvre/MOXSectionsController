- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSString *cellID = [NSString stringWithFormat:@"Cell_%i_%i", indexPath.section, indexPath.row];
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellID];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:cellID];
        
        UILabel *lblCellID = [[UILabel alloc] initWithFrame:CGRectMake(50, 10, 200, 20)];
        [lblCellID setText:[NSString stringWithFormat:@"-> #%i", indexPath.row]];
        [lblCellID setBackgroundColor:[UIColor clearColor]];
        [lblCellID setTextColor:[UIColor blackColor]];
        
        [cell addSubview:lblCellID];
    }
    
    return cell;
}

